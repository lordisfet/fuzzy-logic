package com.gmail.astroidchannel.membershipFunctions;

/**
 * Визначає базові стани (умови) параметрів мікроклімату після оцінки нечітких правил.
 *
 * Бізнес-контекст: В архітектурі системи (Backend Server) цей перелік використовується
 * для класифікації поточного стану середовища (температури, вологи, $CO_2$) у пташнику.
 * Залежно від визначеного стану, система приймає рішення щодо ініціалізації подій
 * та сповіщення персоналу через Telegram API або SMS-шлюз.
 *
 * NFR (Безпека та Продуктивність): Використання type-safe переліку (enum) замість
 * рядкових констант запобігає помилкам маршрутизації сповіщень та гарантує
 * стабільну роботу Spring Boot сервера при одночасній обробці телеметрії від багатьох вузлів.
 */
public enum Condition {

    /**
     * Параметри мікроклімату знаходяться в безпечних межах норми.
     * Втручання системи управління (наприклад, зміна обертів вентиляції) не потрібне.
     */
    NORMAL,

    /**
     * Незначне відхилення показників від норми (перехідна зона).
     * Відповідає події "Warning" — попередження про наближення параметру до порогового значення.
     */
    SUBNORMAL,

    /**
     * Критичне порушення мікроклімату, показники вийшли за допустимі порогові значення.
     * Відповідає події "Critical situation" і вимагає негайного втручання персоналу
     * або автоматичної активації світлозвукової сигналізації (AVAS).
     */
    NOT_NORMAL
}