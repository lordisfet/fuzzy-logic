package com.gmail.astroidchannel.membershipFunctions;

import com.google.common.collect.Range;

import java.util.Set;

/**
 * Контракт для математичних функцій належності нечітких множин.
 *
 * Бізнес-контекст: Використовується для перетворення чітких фізичних
 * показників (наприклад, концентрація аміаку або $CO_2$) у ступінь
 * належності до лінгвістичного терміну (наприклад, "Критично_Високий").
 *
 * NFR (Продуктивність та Масштабованість): Згідно з архітектурою (Зображення 3),
 * усі імплементації цього інтерфейсу працюють на Backend-сервері (Spring Boot)
 * і повинні бути потокобезпечними (thread-safe) та не мати стану (stateless).
 * Вони паралельно обробляють агрегований потік телеметрії від вимірювальних
 * вузлів (N1-N6, Зображення 2).
 */
public interface MembershipFunction {

    /**
     * Обчислює ступінь належності $\mu(x)$ для заданого чіткого значення.
     *
     * @param x Чітке вхідне значення (crisp input) з вимірювального вузла.
     * @return Ступінь належності в діапазоні [0.0, 1.0], де 0.0 означає абсолютну
     *         неналежність, а 1.0 — повну належність до множини.
     */
    double calculate(double x);

    /**
     * Знаходить висоту (height) нечіткої множини.
     *
     * Математичний зміст: Супремум функції належності. Оскільки наша система
     * управління мікрокліматом працює виключно з нормалізованими множинами,
     * цей метод у конкретних реалізаціях зазвичай повертає 1.0.
     *
     * @return Максимальне значення $\mu(x)$, яке може досягти функція.
     */
    double findHeight();

    /**
     * Визначає носій (carrier / support) нечіткої множини.
     *
     * Бізнес-логіка: Повертає діапазон фізичних значень (наприклад, температур),
     * за яких ступінь належності є строго більшим за нуль: $\mu(x) > 0$.
     * Це весь "робочий діапазон", де дане правило впливає на керування кліматом.
     *
     * @return Відкритий або закритий проміжок значень (Range з бібліотеки Guava).
     */
    Range<Double> findCarrier();

    /**
     * Визначає ядро (core) нечіткої множини.
     *
     * Бізнес-логіка: Повертає діапазон значень, за яких ступінь належності
     * дорівнює абсолютному максимуму: $\mu(x) = 1.0$. Це зона "100% впевненості"
     * системи (наприклад, ідеальна норма клімату, або ж стан 100% тривоги,
     * який вимагає негайної активації AVAS).
     *
     * @return Проміжок значень ядра.
     */
    Range<Double> findCore();

    /**
     * Знаходить спектр (граничну зону / boundary) нечіткої множини.
     *
     * Бізнес-логіка: Повертає множину проміжків, де функція змінює своє значення
     * ($0 < \mu(x) < 1$). В архітектурі нашого Backend-сервера це "перехідна зона",
     * в якій система фіксує тренд на погіршення показників та ініціює подію
     * "Warning" до настання "Critical situation".
     *
     * @return Множина проміжків (Set of Ranges), що відповідають схилам функції.
     */
    //TODO: Maybe i can tree use a simple analise with searching max/min value of function in exchange
    // for the override methods
    Set<Range<Double>> findSpectrum();

    /**
     * Утилітний метод для симетричної інверсії нормалізованого значення.
     *
     * NFR (Оптимізація пам'яті): Використовується як stateless-функція для
     * швидкого розрахунку спадних схилів (наприклад, правої частини трапеції)
     * без необхідності інстанціювання додаткових математичних об'єктів у RAM.
     *
     * @param y Значення ступеня належності $\mu(x)$ в діапазоні [0.0, 1.0].
     * @return Інвертоване значення за формулою $1 - y$.
     */
    static double invert0to1Value(double y) {
        return 1.0 - y;
    }

    // TODO: isNormal
    // TODO: Normalization
    // TODO: isConvex
    // TODO: findAlphaLevelSet
}
