package com.gmail.astroidchannel.membershipFunctions.curvesTypes;

import static java.lang.Math.*;

/**
 * Утилітний клас-фабрика для генерації математичних перехідних кривих (Transition Curves).
 *
 * Бізнес-контекст: Ці криві використовуються для побудови функцій належності нечітких множин
 * (наприклад, "Температура", "Рівень $CO_2$"). Вони визначають, як саме чітке фізичне значення
 * з сенсорів IoT-вузлів трансформується у ступінь належності.
 *
 * NFR (Продуктивність та Архітектура): Згідно з архітектурою системи на базі Spring Boot,
 * методи цього класу викликаються конкурентно пулом потоків для обробки телеметрії.
 * Усі методи повертають чисті (stateless) лямбда-вирази з часовою складністю $O(1)$, що гарантує
 * абсолютну потокобезпечність (thread-safety) та мінімальне навантаження на Garbage Collector.
 */
public final class CurveCalculation {

    private CurveCalculation() {
        throw new IllegalStateException("Utility class");
    }

    /**
     * Генерує лінійну перехідну криву.
     *
     * Математична модель: Обчислюється за формулою $\frac{t - a}{b - a}$.
     * Використовується для простих кліматичних параметрів, де зміна стану відбувається рівномірно.
     *
     * @param a Точка початку переходу на осі абсцис (ступінь належності 0.0).
     * @param b Точка завершення переходу на осі абсцис (ступінь належності 1.0).
     * @return Лямбда-вираз, що обчислює лінійну зміну.
     */
    public static TransitionCurve getLinear(double a, double b) {
        return t -> (t - a) / (b - a);
    }

    /**
     * Генерує косинусоїдальну (гладку) перехідну криву.
     *
     * Бізнес-логіка: На відміну від лінійної кривої, косинусоїда забезпечує плавний старт
     * та плавне завершення переходу за формулою $\frac{1}{2} + \frac{\cos(\pi \frac{t - a}{b - a})}{2}$.
     * Це критично важливо при формуванні керуючих сигналів для потужної витяжної вентиляції,
     * щоб уникнути різких стрибків напруги при зміні обертів моторів.
     *
     * @param a Точка початку плавного переходу.
     * @param b Точка завершення плавного переходу.
     * @return Лямбда-вираз, що обчислює косинусоїдальну зміну.
     */
    public static TransitionCurve getCosine(double a, double b) {
        return t -> 1.0 / 2.0 + Math.cos(Math.PI * (t - a) / (b - a)) / 2.0;
    }

    /**
     * Генерує сигмоїдну (S-подібну) перехідну криву.
     *
     * Математична модель: Обчислюється за логістичною формулою $\frac{1}{1 + e^{-slope(t - inflectionPoint)}}$.
     * Ідеально підходить для моделювання небезпеки насичення концентрації газів (наприклад, аміаку $NH_3$),
     * де після досягнення точки перегину рівень загрози для птиці зростає експоненційно.
     *
     * @param slope Крутизна кривої (швидкість зростання функції).
     * @param inflectionPoint Точка перегину, де ступінь належності дорівнює точно 0.5.
     * @return Лямбда-вираз, що обчислює сигмоїдну зміну.
     */
    public static TransitionCurve getSigmoid(double slope, double inflectionPoint) {
        return t -> 1.0 / (1.0 + Math.exp(-slope * (t - inflectionPoint)));
    }

    /**
     * Генерує узагальнену дзвоноподібну (Bell-shaped) перехідну криву.
     *
     * Математична модель: Розраховується за формулою $\frac{1}{1 + |\frac{t - c}{a}|^{2b}}$.
     * Дозволяє гнучко налаштовувати ширину та крутизну "дзвона". Використовується для
     * опису лінгвістичного терміну "Норма" для показників температури чи вологості.
     *
     * @param a Параметр, що визначає ширину дзвона.
     * @param b Параметр, що визначає крутизну схилів (чим більше, тим крутіший схил).
     * @param c Центр дзвона (точка ідеальної належності, де $\mu(t) = 1.0$).
     * @return Лямбда-вираз, що обчислює дзвоноподібну функцію.
     */
    public static TransitionCurve getBell(double a, double b, double c) {
        return t -> 1.0 / (1.0 + Math.pow(Math.abs((t - c) / a), 2 * b));
    }

    /**
     * Генерує функцію Гаусса (нормальний розподіл).
     *
     * Бізнес-логіка: Обчислюється за формулою $e^{-\frac{(t-c)^2}{2s^2}}$.
     * Найкраще застосовується для природних фізичних явищ (наприклад, теплового розподілу
     * в ангарі довжиною 100 метрів), оскільки вона статистично найбільш точно описує
     * відхилення від цільового показника (setpoint).
     *
     * @param c Математичне очікування (центр симетрії, ідеальний кліматичний показник).
     * @param s Середньоквадратичне відхилення (ширина розкиду або толерантність до відхилень).
     * @return Лямбда-вираз, що обчислює функцію Гаусса.
     */
    public static TransitionCurve getGauss(double c, double s) {
        return t -> pow(E, -pow(t-c, 2)/(2 * pow(s, 2)));
    }
}