package com.gmail.astroidchannel.membershipFunctions.curvesTypes;

/**
 * Функціональний інтерфейс для представлення математичної перехідної кривої.
 *
 * Бізнес-контекст: Згідно з архітектурою (Backend Server), цей інтерфейс
 * використовується для визначення лямбда-виразів, що описують функції належності
 * (наприклад, Гаусса або сигмоїду). Він обробляє нормалізовані дані від
 * Edge-вузлів (N1-N6) для визначення критичних станів мікроклімату.
 *
 * NFR (Продуктивність): Використання анотації FunctionalInterface гарантує, що
 * розрахунки $\mu(t)$ виконуються без накладних витрат на створення об'єктів
 * (stateless execution), що є критичним для стабільності Java-контейнера при
 * обробці тисяч пакетів телеметрії на секунду.
 */
@FunctionalInterface
public interface TransitionCurve {

    /**
     * Обчислює ступінь переходу (належності) для заданого вхідного параметра.
     *
     * @param t Чітке вхідне значення (наприклад, показник температури або $CO_2$,
     *          отриманий по локальній мережі від LS-шлюзу).
     * @return Значення ступеня належності в діапазоні [0.0, 1.0], що відображає
     *         результат обчислення математичної кривої.
     */
    double calculate(double t);
}